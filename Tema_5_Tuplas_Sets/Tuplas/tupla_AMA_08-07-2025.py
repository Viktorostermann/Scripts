''' Las tuplas son elementos inmutables, es decir, no se pueden modificar. 
Las tuplas solo se pueden utilizar para almacenar datos que no cambian. 
Las tuplas son mas rapidas que las listas
Las tuplas ocupan menos espacio en memoria que las listas
Las tuplas son mas seguras que las listas
Las tuplas ocupan menos espacio en memoria que las listas
Si requerimos guardar muchos elementos en una sola variable, y en el futuro solo queremos recorrerlos para verlos es mejor usar tuplas
Las tuplas Pueden ser como llaves de un diccionario '''

mi_tupla = (10, 20, 30, 40, 50)

# Imprimir la tupla
print(mi_tupla)
print(type(mi_tupla))

# Acceder a un elemento de la tupla
print(mi_tupla[0])
hola = mi_tupla[0]
print(hola)

# Acceder a los datos de las tuplas
print(mi_tupla[1])
print(mi_tupla[2])
print(mi_tupla[3])
